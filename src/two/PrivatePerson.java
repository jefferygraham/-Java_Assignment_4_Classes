package two;

public class PrivatePerson {
	
	private String name;
	private int age;
	
	//getters
	
	public String getName() {
		return this.name;
	}
	
	public int getAge() {
		return this.age;
	}
	
	//setters
	public void setName(String newName) {
		this.name = newName;
	}
	
	public void setAge(int newAge) {
		this.age = newAge;
	}

}
